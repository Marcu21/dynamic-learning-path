ModuleInsertionStateDict = Dict[str, Any]
"""Type alias for module insertion state as dictionary."""

def create_initial_state(
    user_query: str,
    learning_path_id: int,
    insert_position: int,
    platform_name: str,
    user_id: str,
    **kwargs
) -> ModuleInsertionState:
    """
    Create initial state for module insertion workflow.

    Args:
        user_query: What the user wants in the module
        learning_path_id: Target learning path ID
        insert_position: Index where the new module will be inserted
        platform_name: Platform to use for content
        user_id: User performing the insertion
        **kwargs: Additional state parameters

    Returns:
        Initialized ModuleInsertionState
    """
    state_data = {
        "user_query": user_query,
        "learning_path_id": learning_path_id,
        "insert_position": insert_position,
        "platform_name": platform_name,
        "user_id": user_id,
        "current_stage": "initialization",
        "total_start_time": datetime.now(),
        "stage_start_time": datetime.now(),
        **kwargs
    }

    return ModuleInsertionState(**state_data)


def validate_platform_name(platform_name: str) -> bool:
    """
    Validate if platform name is supported.

    Args:
        platform_name: Platform name to validate

    Returns:
        True if platform is supported, False otherwise
    """
    supported_platforms = ["youtube", "spotify", "google books", "semantic scholar", "codeforces"]
    return platform_name.lower() in supported_platforms


class ModuleInsertionAsyncRequest(ModuleInsertionRequest):
    """Request model for asynchronous module insertion."""

    priority: Optional[str] = Field("normal", description="Task priority: low, normal, high")
    callback_url: Optional[str] = Field(None, description="Optional webhook URL for completion notification")

    @field_validator('platform_name')
    def validate_platform(cls, v):
        if not validate_platform_name(v):
            supported = ["youtube", "spotify", "google books", "semantic scholar", "codeforces"]
            raise ValueError(f"Platform '{v}' not supported. Supported platforms: {supported}")
        return v.lower()

    @field_validator('priority')
    def validate_priority(cls, v):
        if v not in ["low", "normal", "high"]:
            raise ValueError("Priority must be one of: low, normal, high")
        return v


class AsyncInsertionResponse(BaseModel):
    """Response model for asynchronous module insertion."""

    task_id: str = Field(..., description="Task ID for tracking")
    status: str = Field(..., description="Initial task status")
    message: str = Field(..., description="Status message")
    estimated_completion_time: Optional[str] = Field(None, description="Estimated completion time")
